using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

    public class Coord
    {
        private int x;
        private int y;
        public void setX(int X)
        {
            this.x = X;
        }
        public void setY(int Y)
        {
            this.y = Y;
        }
        public int getX()
        {
            return x;
        }
        public int getY()
        {
            return y;
        }
    }
